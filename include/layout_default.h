static inline uint16_t modifier_mask_for_key(uint8_t code) {
    switch (code) {
    case 0xE0: return MOD_CTRL;
    case 0xE1: return MOD_SHIFT;
    case 0xE2: return MOD_ALT;
    case 0xE3: return MOD_GUI;
    case 0xE4: return MOD_FN;
    default: return 0;
    }
}

